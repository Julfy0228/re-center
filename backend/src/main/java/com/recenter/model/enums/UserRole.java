package com.recenter.model.enums;

/**
 * Роль пользователя в системе.
 * <p>
 * Влияет на доступ к функциям приложения.
 * </p>
 */
public enum UserRole {
    CLIENT,   // обычный клиент
    MANAGER,  // менеджер (может управлять бронированиями и услугами)
    ADMIN     // администратор (полный доступ)
}
